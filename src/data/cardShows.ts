// Coords are approximate city-centre / venue lat-lng — used for the
// upcoming "distance from your city" search on the listing pages.

export interface CardShow {
  id: string
  name: string
  slug: string
  country: 'uk' | 'us'
  region: string
  city: string
  venue?: string
  address?: string
  postcode?: string
  startDate: string                 // ISO yyyy-mm-dd
  endDate?: string                  // ISO yyyy-mm-dd, omit for single-day
  recurring?: string                // e.g. "monthly", "first sunday of the month"
  eventType: 'pokemon' | 'tcg' | 'card-show' | 'collectibles' | 'mixed'
  description: string
  organiserName?: string
  websiteUrl?: string
  ticketUrl?: string
  instagramUrl?: string
  facebookUrl?: string
  imageUrl?: string                 // logo / hero image
  latitude?: number                 // approx venue lat (for distance feature)
  longitude?: number                // approx venue lng
  featured?: boolean
  lastChecked: string               // ISO yyyy-mm-dd
  status: 'upcoming' | 'cancelled' | 'past' | 'unknown'
}

